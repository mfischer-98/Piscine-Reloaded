#include <stdio.h>

void    ft_swap(int *a, int *b)
{
    int t;

    t = *a;
    *a = *b;
    *b = t;
}

int main(void)
{
    int x;
    int y;

    x = 10;
    y = 20;
    ft_swap(&x, &y);
    printf("x = %d\ny = %d", x, y);
    return (0);
}